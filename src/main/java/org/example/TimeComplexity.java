package org.example;

public class TimeComplexity {
    public static void timeComplexity(int param) {

        if (param > 4) {
            System.out.println("dsgfshafsda");
        }
        if (param > 4 && param < 10) {
            System.out.println("asdfsdghg");
        }

        for (int i = 0; i < param; i++) {
            for (int y = 0; y < param; i++) {

                System.out.println(param);
            }
        }


        for (int i = 0; i < param; i++) {
            for (int y = 0; y < param; i++) {

                System.out.println(param);
            }
        }

        // n^2
    }
}
